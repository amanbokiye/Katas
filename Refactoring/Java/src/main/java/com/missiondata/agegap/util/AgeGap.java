package com.missiondata.agegap.util;

/**
 * This class specifies two terms that represent the qualitative type of age
 * gap: CLOSEST smallest age difference between list of pairs of person.
 * FURTHEST largest age difference between list of pairs of person.
 * 
 * @author Aman
 *
 */
public enum AgeGap {
	CLOSEST, FURTHEST
}